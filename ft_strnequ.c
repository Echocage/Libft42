#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!*s2)
		return (1);
	while (n > 0)
	{
		if (*s1++ != *s2++)
			return (0);
		n--;
	}
	return (n == 0 || (*s1 == *s2));
}
