#include "libft.h"

int	ft_isalllower(char *s)
{
	int found;

	found = 0;
	while (*s)
	{
		if (*s >= 'a' && *s <= 'z')
			found = 1;
		if (*s >= 'A' && *s++ <= 'Z')
			return (0);
	}
	return (found);
}
